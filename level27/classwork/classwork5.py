#.upper() მეთოდით შეგვიძლია გავადიდოთ ყველა ასო ჩვენს სტრინფში

#მაგ:
text = "cmoon broo"
x = text.upper()

print(x)

#კიდე:
text = "yooo, how are you"
x = text.upper()

print(x)

#.lower() მეთდით შეგვიძლია ყველა დიდი ასო დავაბრუნოთ ჩვეულებრივ მდგომარეობაში, ანუ დავაპატარავოთ

#მაგ:
text = "CMOON BROO"
x = text.lower()

print(x)

#კიდე:
text1 = "YOOO, HOW ARE YOU"
x = text1.lower()

print(x)
